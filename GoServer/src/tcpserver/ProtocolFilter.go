package tcpserver

type ProtocolFilter interface {
	Parse(buffer []byte, totalRecvSize int) int
}

//-------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------
type NullTerminatedProtocolFilter struct {
}

func (filter NullTerminatedProtocolFilter) Parse(buffer []byte, totalRecvSize int) int {
	for i := 0; i < totalRecvSize; i++ {
		if buffer[i] == 0 {
			return i + 1
		}
	}

	return 0
}
